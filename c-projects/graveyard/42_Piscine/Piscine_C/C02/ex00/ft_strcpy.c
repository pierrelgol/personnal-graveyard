char	*ft_strcpy(char *dest, char *src)
{
	int	i;

	if (dest != (void *)0 && src != (void *)0)
	{
		i = 0;
		while (*src)
			dest[i++] = *src++;
		dest[i] = '\0';
	}
	return (dest);
}
