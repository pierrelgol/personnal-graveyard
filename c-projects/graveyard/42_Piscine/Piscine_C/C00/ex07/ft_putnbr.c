#include <unistd.h>

void	ft_putnbr(int nb)
{
	char	*ch;
	long	n;
	int		i;

	ch = (char [13]){0};
	n = nb;
	if (n <= 0)
	{
		if (n == 0)
			write(1, &"0", 1);
		else
		{
			write(1, &"-", 1);
			n *= -1;
		}
	}
	i = 0;
	while (n > 0)
	{
		ch[i++] = "0123456789"[(n % 10)];
		n = n / 10;
	}
	while (i > 0)
		write(1, &ch[--i], 1);
}
