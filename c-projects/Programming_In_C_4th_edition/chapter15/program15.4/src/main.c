//--------------------[     DEPENDENCIES DECLARATION     ]--------------------//

#include <stdio.h>
#include <stdlib.h>

//--------------------[     MAIN METHOD     ]--------------------------------//

// Program to echo characters until an end of file
int main(void) {
  int c;
  while ((c = getchar()) != EOF)
    putchar(c);
  return 0;
}
