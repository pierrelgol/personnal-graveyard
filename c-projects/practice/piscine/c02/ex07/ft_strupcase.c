char	*ft_strupcase(char *str)
{
	int	i;

	if (str != (void *)0)
	{
		i = 0;
		while (str[i])
		{
			if ((str[i] >= 'a' && str[i] <= 'z'))
				str[i] = (str[i] - 'a') + 'A';
			++i;
		}
		return (str);
	}
	return ((void *)0);
}
