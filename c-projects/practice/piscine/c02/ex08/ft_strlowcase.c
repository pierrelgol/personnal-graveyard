char	*ft_strlowcase(char *str)
{
	int	i;

	if (str != (void *)0)
	{
		i = 0;
		while (str[i])
		{
			if ((str[i] >= 'A' && str[i] <= 'Z'))
				str[i] = (str[i] - 'A') + 'a';
			++i;
		}
		return (str);
	}
	return ((void *)0);
}
