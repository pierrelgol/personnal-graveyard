int	ft_str_is_alpha(char *str)
{
	if (!str || *str == '\0')
		return (1);
	while ((*str >= 'a' && *str <= 'z') || (*str >= 'A' && *str <= 'Z'))
		str++;
	if (*str == '\0')
		return (1);
	return (0);
}
